// Trocando Valores de Variáveis

let vermelho = 'vermelho';
let azul = 'azul';

console.log(vermelho);
console.log(azul);

let aux; // Variável auxiliar para guardar o valor de uma das variáveis durante a troca

aux = vermelho;
vermelho = azul;
azul = aux;

console.log(vermelho);
console.log(azul);